/**
 * Build influnet/data/india-states-cities.js from dr5hn countries-states-cities-database.
 * Source: https://github.com/dr5hn/countries-states-cities-database (ODbL, attribution required)
 *
 * Usage: node scripts/build-india-locations.js [path-to-countries+states+cities.json]
 */
const fs = require("fs");
const path = require("path");

const inputPath =
  process.argv[2] ||
  path.join(__dirname, "../tmp-countries-states-cities.json");
const outPath = path.join(__dirname, "../influnet/data/india-states-cities.js");

if (!fs.existsSync(inputPath)) {
  console.error("Missing input:", inputPath);
  console.error("Download:");
  console.error(
    "  https://raw.githubusercontent.com/dr5hn/countries-states-cities-database/master/json/countries%2Bstates%2Bcities.json"
  );
  process.exit(1);
}

const raw = JSON.parse(fs.readFileSync(inputPath, "utf8"));
const india = raw.find((c) => c.name === "India" || c.iso2 === "IN");
if (!india?.states?.length) {
  console.error("India not found in dataset");
  process.exit(1);
}

const states = [];
const citiesByState = {};

for (const st of india.states) {
  const stateName =
    typeof st === "string" ? st : st.name || st.state_name || st.state;
  if (!stateName) continue;

  states.push(stateName);

  let cities = [];
  if (typeof st === "object" && Array.isArray(st.cities)) {
    cities = st.cities
      .map((c) => (typeof c === "string" ? c : c.name))
      .filter(Boolean);
  }

  cities = [...new Set(cities)].sort((a, b) => a.localeCompare(b, "en", { sensitivity: "base" }));
  citiesByState[stateName] = cities;
}

const payload = { states, citiesByState };
const totalCities = Object.values(citiesByState).reduce((n, arr) => n + arr.length, 0);

const js = `/**
 * India states → cities for signup & profile forms.
 * Generated by scripts/build-india-locations.js
 * Data: dr5hn/countries-states-cities-database (ODbL)
 * @type {{ states: string[], citiesByState: Record<string, string[]> }}
 */
window.INFLUNET_INDIA_STATES_CITIES = ${JSON.stringify(payload, null, 2)};
`;

fs.writeFileSync(outPath, js, "utf8");
console.log(`Wrote ${outPath}`);
console.log(`  ${states.length} states/UTs, ${totalCities} cities`);
